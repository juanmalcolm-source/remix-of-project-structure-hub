// ── Helpers ───────────────────────────────────────────────────────────

/** Get festivals relevant for a given genre or speciality keyword */
export function filterFestivalsBySpecialty(keyword: string): FestivalCatalog[] {
  const kw = keyword.toLowerCase();
  return FESTIVALES_CATALOGO.filter(
    (f) =>
      f.especialidad.some((e) => e.toLowerCase().includes(kw)) ||
      f.categorias.some((c) => c.toLowerCase().includes(kw)) ||
      f.nombre.toLowerCase().includes(kw),
  );
}

/** Get festivals by country */
export function filterFestivalsByCountry(country: string): FestivalCatalog[] {
  return FESTIVALES_CATALOGO.filter((f) => f.pais.toLowerCase() === country.toLowerCase());
}

/** Get only high-relevance festivals for Spanish productions */
export function getHighRelevanceFestivals(): FestivalCatalog[] {
  return FESTIVALES_CATALOGO.filter((f) => f.relevancia_espana <= 2);
}

/** Get festivals by clase */
export function filterFestivalsByClase(clase: FestivalCatalog['clase']): FestivalCatalog[] {
  return FESTIVALES_CATALOGO.filter((f) => f.clase === clase);
}

/** Build a summary string for AI prompts */
export function buildFestivalsSummaryForAI(): string {
  return FESTIVALES_CATALOGO.filter((f) => f.relevancia_espana <= 2)
    .map(
      (f) =>
        `${f.nombre} (${f.pais}, ${f.clase}, ${f.meses}) — ${f.especialidad.join(', ')}${f.cualifica_premios.length > 0 ? ` [Cualifica: ${f.cualifica_premios.join(', ')}]` : ''}`,
    )
    .join('\n');
}
